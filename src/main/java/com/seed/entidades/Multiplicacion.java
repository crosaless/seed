
package com.seed.entidades;

/**
 * Clase que realiza la operación de multiplicación de dos números de tipo doble.
 */
public final class Multiplicacion {

    /**
     * Multiplica dos números de tipo doble.
     *
     * @param a el primer número de tipo doble.
     * @param b el segundo número de tipo doble.
     * @return el producto de los dos números.
     */
    public double multiplicar(final double a, final double b) {
        return a * b;
    }
}

